class Graph:

    """Represent a graph as a dictionary of vertices mapping labels to edges."""

    def __init__(self):
        self.vertices = {}

    def add_vertex(self, vertex_id):
        """
        Add a vertex to the graph.
        """
        if vertex_id not in self.vertices:
            self.vertices[vertex_id] = set()

    def add_edge(self, v1, v2):
        """
        Add a directed edge to the graph.
        """
        try:
            # will throw error if v2 key not in vertices
            check = self.vertices[v2]
            self.vertices[v1].add(v2)
        except KeyError:
            print(f'There is no "{v2}" vertex')

    def get_neighbors(self, vertex_id):
        """
        Get all neighbors (edges) of a vertex.
        """
        return self.vertices[vertex_id]


    def dft(self, starting_vertex):
        """
        Print each vertex in depth-first order
        beginning from starting_vertex.
        """
        # create a stack
        scheduled = Stack()
        # add the starting node to stack
        scheduled.push([starting_vertex])
        visited = set()
        path = []
        # while verts scheduled to be visited
        while scheduled.size() > 0:
            # remove the first item visiting
            path = scheduled.pop()
            print('PATH: ', path)
            current_vertex = path[-1]
            # if not visited yet
            if current_vertex not in visited:
                visited.add(current_vertex)
                # go through the neighbors
                for next_vertex in self.get_neighbors(current_vertex):
                    # schedule the node to visit it later
                    path_copy = list(path)
                    path_copy.append(next_vertex)
                    # add the new path to the stack
                    scheduled.push(path_copy)
                    print('SCHEDULED: ', scheduled.stack)

        return path


